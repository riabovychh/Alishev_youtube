public class Lesson5_For {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) { // первая часть - переменная-счетчик (указывается стартовое значение),
            // вторая часть - блок условия (до каких пор будет выполнятся цикл),
            // третья часть - как меняется переменная после цикла
            // i++ - то же самое, что i=i+1
           System.out.println("Hello "+i);
        }
        }
    }
